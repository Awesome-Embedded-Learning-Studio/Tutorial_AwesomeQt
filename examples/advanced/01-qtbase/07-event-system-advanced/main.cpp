/// @file    main.cpp
/// @brief   Qt 事件系统进阶示例的程序入口。
///
/// 对应教程：进阶层 01-QtBase/07-事件系统进阶。
/// 演示自定义事件类、跨线程 postEvent、eventFilter 全局监听。

#include "custom_event.h"
#include "event_filter_demo.h"

#include <QApplication>
#include <QDebug>
#include <QTimer>

// ============================================================================
// 主函数
// ============================================================================
int main(int argc, char* argv[])
{
    QApplication app(argc, argv);

    qDebug() << "========== Qt 事件系统进阶示例 ==========\n";

    // ----------------------------------------------------------------------
    // 演示 1: 自定义事件与 registerEventType
    // ----------------------------------------------------------------------
    qDebug() << "[演示 1] 自定义事件类型";
    qDebug() << "自定义事件 ID:" << MessageEvent::eventId;
    qDebug() << "QEvent::User 起始值:" << QEvent::User;

    // ----------------------------------------------------------------------
    // 演示 2: sendEvent vs postEvent
    // ----------------------------------------------------------------------
    qDebug() << "\n[演示 2] sendEvent vs postEvent";
    demoSendVsPost();

    // ----------------------------------------------------------------------
    // 演示 3: 跨线程 postEvent
    // ----------------------------------------------------------------------
    qDebug() << "\n[演示 3] 跨线程 postEvent";
    demoCrossThreadEvent();

    // ----------------------------------------------------------------------
    // 演示 4: 事件过滤器
    // ----------------------------------------------------------------------
    qDebug() << "\n[演示 4] 事件过滤器";
    demoEventFilter();

    // ----------------------------------------------------------------------
    // 结束
    // ----------------------------------------------------------------------
    qDebug() << "\n===========================================";
    qDebug() << "要点总结:";
    qDebug() << "  - 继承 QEvent 创建自定义事件，用 registerEventType() 注册";
    qDebug() << "  - sendEvent 同步处理，postEvent 异步投递";
    qDebug() << "  - customEvent() 接收自定义事件";
    qDebug() << "  - eventFilter() 可拦截任何事件，实现全局监听";

    // 使用 QTimer 自动退出（避免阻塞事件循环）
    QTimer::singleShot(0, &app, &QApplication::quit);
    return app.exec();
}
